const cups = 41;
        
switch (cups) {
    case 41:
        console.log("the value of cups is 41");
        break;

    case 42:
        console.log("the value of cups is 42");
        break;

    case 43:
        console.log("the value of cups is 43");
        break;

    case 44:
        console.log("the value of cups is 44");
        break;

    default:
        console.log("the value of cups is not 41,42,43,44")
        break;
}